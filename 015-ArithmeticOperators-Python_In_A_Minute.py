"""
| Operator               | Sign | Explanation                                       |
|------------------------|------|---------------------------------------------------|
| Addition               |  +   | Adds two operands                                 |
| Subtraction            |  -   | Subtracts right operand from left                 |
| Multiplication         |  *   | Multiplies two operands                           |
| Division               |  /   | Divides left operand by right                     |
| Floor Division         |  //  | Divides and returns the integer value of the quotient |
| Modulus (Remainder)    |  %   | Returns remainder of the division                 |
| Exponentiation         |  **  | Raises left operand to the power of right operand |
"""

a = 7
b = 3

result = a + b
print(result)

# Output: 10

result = a - b
print(result)

# Output: 4

result = a * b
print(result)

# Output: 21

result = a / b
print(result)

# Output: 2.3333333333333335

result = a // b
print(result)

# Output: 2

result = a % b
print(result)

# Output: 1

result = a ** b
print(result)

# Output: 343

