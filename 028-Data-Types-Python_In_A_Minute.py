# Python Data Types


# Numeric Types
print("\nNumeric Types:")
# Integer
int_example = 5
print("Integer:", int_example)

# Float
float_example = 5.0
print("Float:", float_example)

# Complex
complex_example = 5 + 2j
print("Complex:", complex_example)

# Sequence Types
print("\nSequence Types:")
# String
str_example = "Hello"
print("String:", str_example)

# List
list_example = [1, 2, 3]
print("List:", list_example)

# Tuple
tuple_example = (1, 2, 3)
print("Tuple:", tuple_example)

# Mapping Type
print("\nMapping Type:")
# Dictionary
dict_example = {'key': 'value'}
print("Dictionary:", dict_example)

# Set Types
print("\nSet Types:")
# Set
set_example = {1, 2, 3}
print("Set:", set_example)

# Frozenset
frozenset_example = frozenset({1, 2, 3})
print("Frozenset:", frozenset_example)

# Boolean Type
print("\nBoolean Type:")
# Boolean
bool_example_true = True
bool_example_false = False
print("Boolean True:", bool_example_true)
print("Boolean False:", bool_example_false)

# None Type
print("\nNone Type:")
# None
none_example = None
print("None:", none_example)

